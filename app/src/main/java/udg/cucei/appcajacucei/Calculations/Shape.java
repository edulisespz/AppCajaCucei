public class Shape
{
    int x, y, z;
    public Shape(int a, int b, int c)
    {
        x = a;
        y = b;
        z = c;
    }

    public int getX()
{
    return x;
}

    public int getY()
{
    return y;
}

    public int getZ()
{
    return z;
}
}
